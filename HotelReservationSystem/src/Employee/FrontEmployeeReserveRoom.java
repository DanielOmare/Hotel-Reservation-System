    private void requestRoomButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestRoomButtonActionPerformed
        // TODO add your handling code here:
        Room room = (Room)jList1.getSelectedValue();
        Date arrive_date = jXDatePicker1.getDate();
        Date departure_date = jXDatePicker2.getDate();
        if(arrive_date==null || departure_date == null)
        {
            statusLabel.setText("Please pick dates");
        }
        else if(room==null)
        {
            statusLabel.setText("Select room");
        }
        else
        {
            if(db.reserveRoomFrontEmployee(customer_id, branch_id, room , arrive_date, departure_date))
            {
                JOptionPane.showMessageDialog(null, "Reservation is done!!");
                this.dispose();
            }
            else
            {
                statusLabel.setText("Reservation is not done!!");
            }
        }
    }//GEN-LAST:event_requestRoomButtonActionPerformed

