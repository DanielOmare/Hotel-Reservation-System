    public void fillRoomList()
    {
        try {
            ArrayList<Room> rooms = new ArrayList<Room>();
            String query ="select room.ROOM_NUMBER, room_type.MAX_GUESTS, room_type.NAME, room_type.PRICE, room_type.TOTAL_ROOMS from room_type, room where room.TYPE_ID = room_type.id "
                    + "and status = 'empty' and room.branch_id = "+branch_id;
            
            ResultSet rs = db.getResultOfQuery(query);
            while(rs.next())
            {
                Room room = new Room(rs.getInt(1), rs.getString(3), rs.getInt(2), rs.getInt(5), rs.getInt(4));
                room.setBranch_id(branch_id);
                rooms.add(room);
 
            }
            jList1.setListData(rooms.toArray());
            
            
        } catch (SQLException ex) {
            Logger.getLogger(FrontEmployeeReserveRoom.class.getName()).log(Level.SEVERE, null, ex);
        }
        
                
        
    }

