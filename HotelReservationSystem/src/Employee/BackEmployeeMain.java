    public void fillSearchList()
    {
        String query="";
        
       
            
             if(searchByCmb.getSelectedItem().toString().equals("By ID"))
        {
            query = "select id, branch_id, activity_name, start_time, end_time, info from activity where id like '"+searchField.getText()+"%'";
        }
        else if(searchByCmb.getSelectedItem().toString().equals("By Name"))
        {
            query = "select id, branch_id, activity_name, start_time, end_time, info from activity where activity_name like '"+searchField.getText()+"%'";
        }

        //JOptionPane.showMessageDialog(null, query);
        
        ArrayList<Activity> list = new ArrayList<Activity>();
        ResultSet rs = db.getResultOfQuery(query);
        int count = 0;
        try {
            while(rs.next())
            {
                count++;
                Date start_date = rs.getTimestamp(4);
                Date end_date = rs.getTimestamp(5);
                Activity activity = new Activity(rs.getInt(1), rs.getInt(2), rs.getString(3), start_date, end_date,rs.getString(6));
                list.add(activity);

            }
            jList1.setListData(list.toArray());
            searchResultCount.setText("Number of result : "+count);
        }
        catch(Exception e)
        {
            
        }
    }
    
