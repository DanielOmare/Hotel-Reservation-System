    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(addActivityIDField.getText().trim().equals(""))
        {
            addActivityStatusLbl.setText("Enter ID!!");
        }
        else if(addActivityNameField.getText().trim().equals("") || jTextArea1.getText().trim().equals(""))
        {
            addActivityStatusLbl.setText("Please fill above information");
        }
        else if(jComboBox1.getSelectedItem().toString().equals(""))
        {
            addActivityStatusLbl.setText("Please select Branch");
        }
        else if(start_datePicker.getDate()==null || end_datePicker.getDate()==null)
        {
            addActivityStatusLbl.setText("Please select Date");
        }
        else
        {
            int id = Integer.parseInt(addActivityIDField.getText());
             int branch = Integer.parseInt(jComboBox1.getSelectedItem().toString());
            Date start_date = start_datePicker.getDate();
            Date end_date = end_datePicker.getDate();
            String activityName = addActivityNameField.getText();
            String info = jTextArea1.getText();
        
            if(db.insertActivity(id, branch, activityName, start_date, end_date, info))
            {
                addActivityStatusLbl.setText("Added Successfully!");
                clearAddActivityField();
                
            
            }
            else
        {
            
            addActivityStatusLbl.setText("Not updated");
        }
            
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

